 function pathfnx(index){

        const active= path[index];
        

      const pathTitle = document.querySelector('.pathModal-Title');
      const path_image = document.querySelector('.pathModal-image img');
      const pathTopic =  document.querySelector('.path-Topic');
      const pathDesc = document.querySelector('.path-des');
      const roadmapsCont = document.querySelector('.pathRoadmap');

      pathTitle.innerHTML=active.title;
      pathTopic.innerHTML = active.title;
      path_image.src=active.img;
      pathDesc.innerHTML=active.description;

      let roadmapcards = '';

      let cardArray = active.roadmapCards
      
      cardArray.forEach(card=>{
        roadmapcards +=`
                <div class="roadmapCard">
                    <div class="roadmapCard-title">${card.title}</div>
                    <div class="roadmapCard-description">${card.description}</div>
                </div>
      `
      })

      roadmapsCont.innerHTML = roadmapcards

 }

 function getPathbutton(button,index){
  const pathButton = document.querySelector(button);
  if(!pathButton)return;
  
  pathButton.addEventListener('click',()=>{
      pathfnx(index);
      document.querySelector('.pathModal-overlay').style.display='flex';
  })
 }

